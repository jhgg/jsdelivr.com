          <h3>About</h3>
        <p> jsDelivr is a free CDN (Content Delivery Network) where any web developer can host their files, including CSS, fonts, JavaScript, jQuery plugins, etc. </p>
<p>jsDelivr is a public open-source CDN where anyone can submit a project to be hosted and delivered by our network.<br/>
Most people probably know that Google hosts jQuery and a few other popular libraries.<br/> We don’t look at the popularity of the project to approve it for inclusion in jsDelivr.
Plus a lot more types of projects are allowed, like CSS frameworks, fonts, WordPress plugins etc. <br/>
And by using GitHub, we allow the community to fully interact with jsDelivr by adding and updating files.</p>
          <h2>System</h2>
          <p>What makes it super-fast is the unique multi-CDN infrastructure we use. MaxCDN (NetDNA) and CDN.net (which is powered by the huge federated network of OnApp) agreed to combine their powers for the good of the open source community and to sponsor us and become our CDN providers.<br/>
All traffic to our CDN providers is load balanced based on availability and performance for each individual user by another official sponsor of ours, the company Cedexis. <br/>This unique system not only gives us the best possible uptime and performance but also allows us to expand dynamically and add new providers on the fly.</p>
          <h2>Uptime</h2>
          <p>All CDNs can go down and most have. But this is not the case with jsDelivr because we use 2 CDN providers.
If any of them goes down all traffic automatically will be switched to the remaining operational CDN.<br/>
jsDelivr is the only public CDN to use multiple providers which makes it the most reliable of them all.</p>
          <h2>Security</h2>
          <p>All files are hosted on a Push zone on NetDNA servers. The only way to access the files is by FTP using a whitelisted IP address.</p>
